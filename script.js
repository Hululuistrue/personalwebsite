function setLanguage(lang) {
  const dictionary = translations[lang] || translations.en;
  const resolvedLanguage = translations[lang] ? lang : "en";

  document.documentElement.lang = resolvedLanguage === "zh" ? "zh-CN" : "en";
  document.title = dictionary.pageTitle;

  const metaDescription = document.getElementById("meta-description");
  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.dataset.i18nAria;
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === resolvedLanguage);
  });

  try {
    localStorage.setItem("siteLanguage", resolvedLanguage);
  } catch (error) {
    // Ignore storage errors; language still applies in current session.
  }
}

const revealElements = document.querySelectorAll(".reveal");

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

revealElements.forEach((element, index) => {
  element.style.animationDelay = `${index * 55}ms`;
  revealObserver.observe(element);
});

const orbs = document.querySelectorAll(".orb");
window.addEventListener("pointermove", (event) => {
  const x = event.clientX / window.innerWidth - 0.5;
  const y = event.clientY / window.innerHeight - 0.5;

  orbs.forEach((orb, index) => {
    const factor = (index + 1) * 10;
    orb.style.transform = `translate(${x * factor}px, ${y * factor}px)`;
  });
});

const yearElement = document.getElementById("year");
if (yearElement) {
  yearElement.textContent = new Date().getFullYear();
}

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

let initialLanguage = "en";
try {
  const storedLanguage = localStorage.getItem("siteLanguage");
  if (storedLanguage && translations[storedLanguage]) {
    initialLanguage = storedLanguage;
  }
} catch (error) {
  // Keep default language as English.
}

setLanguage(initialLanguage);
